def print_rhombus(n):
    # Горна половина на ромба
    for i in range(1, n + 1):
        # Използваме резултат от делението на два за да центрираме ромба
        print(" " * (n - i) + "* " * i)

    # Долната половина на ромба
    for i in range(n - 1, 0, -1):
        print(" " * (n - i) + "* " * i)

# Четем входа на потребителя за размера на ромба
N = int(input())

# Проверка дали числото е положително
if N > 0:
    print_rhombus(N)
else:
    print()
